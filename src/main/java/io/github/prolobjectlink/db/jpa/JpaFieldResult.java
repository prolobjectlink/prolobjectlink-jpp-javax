package io.github.prolobjectlink.db.jpa;

public final class JpaFieldResult {

	private final String name;
	private final String column;

	public JpaFieldResult(String name, String column) {
		this.name = name;
		this.column = column;
	}

	public final String getName() {
		return name;
	}

	public final String getColumn() {
		return column;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((column == null) ? 0 : column.hashCode());
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		JpaFieldResult other = (JpaFieldResult) obj;
		if (column == null) {
			if (other.column != null)
				return false;
		} else if (!column.equals(other.column))
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "JPAFieldResult [name=" + name + ", column=" + column + "]";
	}

}
